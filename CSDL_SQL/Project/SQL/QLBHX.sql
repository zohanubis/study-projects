CREATE TRIGGER Trigger_Check_NhaCungCap_Exists
ON PHIEUNHAP
FOR INSERT, UPDATE
AS
BEGIN
    IF EXISTS (
        SELECT 1
        FROM inserted i
        LEFT JOIN NHACUNGCAP n ON i.MANCC = n.MANCC
        WHERE n.MANCC IS NULL
    )
    BEGIN
        RAISERROR ('Mã Nhà Cung Cấp không hợp lệ', 16, 1);
        ROLLBACK;
        RETURN;
    END;
END;
CREATE TRIGGER Trigger_Check_SanPham_Exists
ON CHITIETHD
FOR INSERT, UPDATE
AS
BEGIN
