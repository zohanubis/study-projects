-- Bảng Sản Phẩm
CREATE TABLE SANPHAM (
    MASP VARCHAR(10) NOT NULL,
    TENSP NVARCHAR(50) ,
	CONSTRAINT PK_SANPHAM PRIMARY KEY (MASP)
);
-- Bảng Khách Hàng
